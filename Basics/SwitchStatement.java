import java.util.*;
public class SwitchStatement {
    public static void main(String args[]){
        int n = 2;
        switch(n){
            case 1: System.out.println("Birani");
            break;
            case 2: System.out.println("Fried Rice");
            break;
            case 3: System.out.println("Murag Polao");
            break;
            default: System.out.println("Daal vaat");

        }
    }
}
